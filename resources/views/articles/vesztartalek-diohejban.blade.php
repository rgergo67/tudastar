        <h2>Mi az a vésztartalék?</h2>

        <p>Dióhéjban: a vésztartalék az a pénz, amit váratlan eseményekre tettél félre.</p>

        <p>És itt valódi vészhelyzetekre gondolj, például autóbalesetet szenvedsz, hónapokra kórházba kerülsz, vagy a vihar leszedi a tetődet <em>(amiről egyre többet hallani manapság)</em>. Ha 70%-os kedvezmény van plazma TV-re, vagy testvérednek esküvője lesz, és te csak a vésztartalékhoz nyúlhatnál, ne tedd! Lesz még leárazás, aki pedig törődik veled, az megérti, ha anyagilag most nem tudod támogatni.</p>

        <p>Hogy miért van szükség vésztartalékra? Egyszerű: <strong>Sosem tudhatod, mi fog történni.</strong> Elképesztően hálás leszel magadnak azért, hogy vésztartalékot képeztél, ha véletlenül elveszíted az állásod, vagy fél év táppénzre kényszerülsz. A COVID megmutatta, hogy mindkettő valós veszély. Ki gondolta volna, hogy a vendéglátás, turizmus és még sok más ágazat így meg fogja sínyleni?</p>

        <blockquote>A vésztartalék egy biztonsági háló közted, és az élet között.</blockquote>

        <h2>Mekkora vésztartalékot gyűjtsek össze?</h2>

        <p>Amennyiben rendelkezel hitellel, érdemes először csak 1-2 havi fizetésednek megfelelő összeget félretenni vésztartaléknak, majd minden <em>felesleges</em> pénzed hitel törlesztésre fordítani. Ha sikerült a hiteleket teljesen visszafizetni, akkor a 6 havi fizetésed mennyisége legyen a vésztartalék cél. <em>(A lakáshitel az egyetlen ami ez alól kivétel, azt jellemzően 10-30 éven keresztül törleszted, ennyit ne várj a vésztartalékkal, kezdd el gyűjteni a lakáshitel törlesztésével párhuzamosan!)</em></p>

        <p>Mindenkire a 6 havi szabály vonatkozik? Nem feltétlenül. Minél stabilabb a jövedelmed, vagy ha részben önellátó módon élsz, lehet kevesebb is. Ha viszont 4 gyermeked van, 3 kutyád, a szüleid is betegesek, akkor gondolj bele, hogy mi történik, ha elveszíted az állásod. Biztosan ki fog tartani a 6 havi vésztartalék? Fogsz találni olyan állást időben, amiből fedezni tudod ezeket a kiadásokat?</p>

        <h2>Hol tároljam a vésztartalékot?</h2>

        <p>A vésztartalékod legyen likvid! Ez azt jelenti, hogy olyan helyen vagy befektetésben tartsd, ahol gyorsan és egyszerűen hozzá tudsz férni.</p>

        <p>Az egyik legjobb ilyen megoldás az <strong>állampapír</strong>, erről <x-a href="https://szendreiadam.hu/befektetes/allampapir-vasarlasa/" target="_blank">Szendrei Ádám írt egy kiváló cikket</x-a>. A <x-a href="https://webkincstar.allamkincstar.gov.hu/" target="_blank">webkincstar.allamkincstar.gov.hu</x-a> oldalon tudod kezdeményezni az ingyenes számlanyitást az államkincstárnál, ami olyan mint egy bankszámla, csak nincs havidíja. Az állampapírt online, bankkártyával is meg tudod venni, így tranzakciós költség sincs, ráadásul jelenleg semmilyen adó nem terheli a kamatokat, amik általában jobbak a banki kamatoknál.</p>

        <p>Mi a vésztartalékunk egy részét inflációkövető állampapírban tartjuk, a másik részét pedig otthon. Utóbbinak több veszélye is van, például betörés esetén egyszerűen lába kél, továbbá kéz alatt van, így ha nincs elég önfegyelmed, könnyű hozzányúlni akkor is, ha nincs veszély, illetve ha 40 éven kersztül porosodik a szekrényben, az értékének jórészét elveszti <em>(az állampapír kamata ezt hivatott kompenzálni)</em>.</p>

        <h2>Mikor használjam a vésztartalékot?</h2>

        <p>Amikor felbukkan egy váratlan kiadás, a legegyszerűbb döntés a vésztartalékhoz nyúlni - azonban nem mindig ez a legbölcsebb választás. Itt van 3 kérdés amiknek megválaszolása segíthet a döntésben:</p>

        <ul>
            <li><strong>Váratlan</strong> kiadás?</li>
            <li><strong>Nélkülözhetetlen</strong> kiadás?</li>
            <li><strong>Nagyon fontos</strong> kiadás?</li>
        </ul>

        <p>Minél többre <em>Igen</em> a válasz, annál biztosabb lehetsz benne, hogy a helyes döntés a vésztartalék használata.</p>

        <h2>Hogyan építs vésztartalékot?</h2>

        <p>Egy biztonsági vésztartalék hatalmas biztonságérzetet tud nyújtani. Így kell felépíteni:</p>

        <h3>1. Készíts egy költségvetést.</h3>

        <p>A beosztom.hu ilyen költségvetés készítésre van kitalálva. Számold össze a teljes vagyonod, nézd meg milyen havi bevételeid vannak, és gyűjtsd össze havi és éves szinten a rendszeres kiadásaidat. Ezek segíteni fognak a következő lépésekben.</p>

        <h3>2. Hozz létre  perselyeket.</h3>

        <p><x-a href="{{ route('knowledge.show', 'kategoriak-es-perselyek') }}">Erről részletesebben itt olvashatsz</x-a>, de a lényeg a következő: a perselyek segítenek kiadások szerint csoportosítani a pénzed. Az csak tőled függ, hogy milyen részletességgel hozod létre a perselyeket, lehet egy darab <em>Számla</em> perselyed, de lehet külön perselyed Áram, Víz, Gáz, Internet névvel is.</p>

        <h3>3. Töltsd fel a nélkülözhetetlen perselyeidet pénzzel.</h3>

        <p>Általában a következő 4 kategóriába tartozó perselyeket szoktuk nélkülözhetetlennek mondani:</p>

        <ul>
            <li>Étel <em>(Étterem, cukrászda nem tartozik ide)</em></li>
            <li>Lakhatás</li>
            <li>Számlák <em>(Netflix, játék előfizetés nem tartozik ide)</em></li>
            <li>Közlekedés <em>(ha kell munkába járáshoz)</em></li>
        </ul>

        <h3>4. Tölsd fel a vésztartalék perselyt.</h3>

        <p>Ha maradt még pénzed, tölsd fel a vésztartalék perselyt. Ha nem maradt, következő fizetésed alkalmával a be nem osztott pénzt tedd ide. Nem muszáj az összeset, hisz kell egy kis kikapcsolódás és szórakozás is, viszont valamennyit mindig tegyél bele.</p>

        <p>Ha bárhonnan beesik egy kis plusz pénz <em>(fizetési bónusz, nyertél, ajándék)</em>, a vésztartalék legyen az első ahova teszel belőle.</p>

        <h2>A vésztartalék feltölése egyszerűen</h2>

        <p>A legegyszerűbb módja a vésztartalék feltöltésének az, ha eladod a felesleges cuccaidat: ruhák, társasjátékok, szerszámok. Az innen befolyt összeget egy az egyben tedd a vésztartalék perselybe, hisz ha eddig jó volt profogónak, akkor állampapírban is jó helye lesz. Meg fogsz lepődni, hogy néhány ezer forint milyen nagy lendületet tud adni a gyűjtögetésnek.</p>

        <p>A másik lehetőség az alkalmi munka. Menj el egyszer napszámba, vállalj bébiszitterkedést, sétáltass kutyát. A lényeg, hogy céltudatosan úgy kezdj neki, hogy a befolyt összegből a vésztartalék perselyt fogod bővíteni.</p>

        <h3>Kezdd el a vésztartalék gyűjtést még ma!</h3>

        <p>Áldozz néhány másodpercet arra, hogy elképzeled azt az állapotot, amikor nincs egy forint hiteled sem, helyette egy 6 hónapos vésztartalék biztosítja a hátadat. Milyen érzés lenne?</p>

        <p>Végre fellélegezhetnél, mert tudnád, hogy bármilyen váratlan esemény is jön, a pénz már nem fog problémát jelenteni, emiatt már nyugodtan aludhatsz.</p>

        <p>Ezzel a tudattal vágj bele a vésztartalék gyűjtésbe! Készíts költségvetést, fizesd vissza a hiteleidet, és építsd mellette a vésztartalékod, apránként! A biztonság érzet és a nyugalom ami ezt követi, megfizethetetlen.</p>

        <p>Elromlik a mosógép, beázik a tető, vagy nem indul az autó? Mindegyik olyan probléma, ami azonnali beavatkozást igényel. Még szerencse, hogy van vésztartalékod, így nem kell azon stresszelned, hogy honnan szerezz hirtelen százezreket. A váratlan krízist sikerült mindössze egy kellemetlenséggé alakítanod.</p>

        <p>Ennek a vésztartaléknak az összegyűjtésében lehet segítségedre a beosztom.hu</p>

