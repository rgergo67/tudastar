<h1>Hatékony pénzbeosztás</h1>

<p>A spórolás egyik legjobb módja, ha a pénzed előre beosztod, ebben lesz segítségedre a beosztom.hu oldala. Ha minden forintnak előre megvan a feladata, nem fogod "véletlenül" elkölteni egy új TV-re azt a pénzt, amiből jövőre nyaralni mehetnél, vagy ami ház felújításra lett félrerakva.</p>

<p>A hatékony pénzbeosztás egyik alapja, hogy jó kategóriákat és perselyeket hozz létre. Két megközelítés lehetséges:</p>

<ol>
    <li>Részletes költségvetés, majd szűkítés</li>
    <li>Nagyvonalú költségvetés, majd bővítés</li>
</ol>

<p>Bármelyiket is választod, a legfontosabb az, hogy vágj bele! Van, akinek nemcsak hogy könnyen megy egy részletes költségvetés elkészítése, de kifejezetten élvezi is. Eközben más már a legelején leblokkolhat, mert nem tudja mivel kezdje. Utóbbi esetben ne bonyolítsd túl, próbáld ki a nagyvonalú költsévetést.</p>

<h2>Részletes költségvetés</h2>

<p>A részletes költségvetés létrehozásához azt javasoljuk, hogy keresd elő az elmúlt 6 hónap banki kimutatásait, illetve szedd össze azokat a kiadásokat amiket készpénzzel fizettél. Tételesen menj végig rajtuk, és hozd létre a kategóriákat, perselyeket.</p>

<p>Ahogy múlik az idő el tudod dönteni, hogy szükséges-e külön Fodrász, Kozmetikus, Pedikűrös persely, vagy inkább egybeolvasztod őket egy Szépségápolás perselybe. Mi az utóbbit szoktuk választani, mert a statisztikában arra vagyunk kíváncsiak, hogy havonta mennyi megy el ezekre összesen.</p>

<h2>Nagyvonalú költségvetés</h2>

<p>Itt néhány általános perselyből indulunk ki, és ahogy múlik az idő, igény szerint bontjuk őket tovább. Egy ilyen költségvetés például így nézhetk ki:</p>

<ul>
    <li>Jótékonyság: ha nem hónapról hónapra élsz, egy kicsi mindig juthat jótékony célra (állat menhely, civil szervezet)</li>
    <li>Tartalék - vésztartalék, nyugdíj, váratlan esemény</li>
    <li>Étel - bevásárlás, étterem, gyorskaja</li>
    <li>Számlák - internet, telefon, áram, gáz, víz</li>
    <li>Lakhatás - albérlet, felújítás, bővítés</li>
    <li>Közlekedés - benzin, szervíz, parkolás, buszjegy</li>
    <li>Egészség - gyógyszer, vizsgálat, kezelés, megelőzés</li>
    <li>Biztosítás - Élet, egészség, lakás, jármű</li>
    <li>Kikapcsolódás - utazás, kondi, élmények, minden ami a testileg és lelkileg rendberak</li>
    <li>Személyes - amit magadra költesz: fodrász, káros szenvedélyek</li>
    <li>Egyéb - ha nem tudod, hogy valami hova tartozik, fizesd ebből</li>
</ul>

<h2>A beosztás</h2>

<p>Ha megvagy a kategóriák és perselyek létrehozásával, kezdődhet a rendelkezésre álló pénzed tényleges beosztása. Mi a következő sorrendet javasoljuk:</p>

<p>Először tegyél pénzt az étel, számlák, albérlet, közlekedés (ha nem otthonról dolgozol) perselyekbe, ezáltal biztosítva van a napi megélhetésed. A maradékból tehetsz a vésztartalék, megtakarítás, biztosítás és hitel visszafizetés perselyekbe.</p>

<p>Elvileg most tartasz ott, hogy mindenre félretettél amire <i>muszáj</i>, a többi mehet szórakozásra, kikapcsolódásra. Fontos, hogy ide is jusson mindig egy kicsi, mert szükségünk van a mentális egészségre és a boldogságra. Egy költségvetés nem azt jelenti, hogy mindenről le kell mondani. Csak gondold végig a fontossági sorrendet, és próbáld annak megfelelően beosztani a pénzed.</p>

<h2>Költségvetés célok</h2>

<p>Ha úgy könnyebbnek érzed, íme néhány cél amit érdemes lehet szemelőtt tartani, ha még nem érted el őket:</p>

<ol>
    <li>Legyen 1 havi fizetésed félretéve vésztartaléknak</li>
    <li>Fizesd vissza az összes hiteled (nyilván ha több éven keresztül kell törlesztened, akkor ezzel párhuzamosan csinálhatod a következőket is)</li>
    <li>Bővítsd ki a vésztartalékod 6 havi fizetésed összegére</li>
    <li>A fizetésed egy részét (10-20%, vagy amennyit megengedhetsz) kezdd el félretenni nyugdíjra, például állampapírba, vagy ami szerinted értékálló</li>
    <li>Kezdj el félretenni a gyerek taníttatására például babakötvénybe</li>
    <li>Ha teheted, támogass egy jótékony szervezetet vagy célt</li>
</ol>
