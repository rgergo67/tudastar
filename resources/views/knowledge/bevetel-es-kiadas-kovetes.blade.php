<h2>4. Bevétel és kiadás követés</h2>

<p>Most már csak annyi a dolgod, hogy amikor pénzt kapsz, akkor azt hozzáadd a beosztható pénzhez, illetve amikor pénzt költesz, valamelyik perselyedből elköltsd az összeget.</p>

<blockquote class="break-all"><p>A bevételt és kiadást egyből rögzítsd, szétosztani ráérsz este vagy hétvégén is!</p></blockquote>

<x-figure name="tranzakcio" title="Egy persely sorára kattintva megjelenik a helyi menü" />

<ul>
    <li><strong>Szerkesztés:</strong> a persely nevét, célját, ikonját tudod szerkeszteni</li>
    <li><strong>Transzfer:</strong> egyik perselyből a másikba tudsz áttenni pénzt</li>
    <li><strong>Beoszt:</strong> az oldal tetején látható beosztható pénzhalomból tudsz pénzt tenni a perselybe</li>
    <li><strong>Kiadás:</strong> ezzel rögzítheted a kiadásaidat</li>
</ul>

<p>Amikor megnyitod a boesztás vagy kiadás ablakot, az Összeg mező felett zöld vagy piros háttérrel ikonokat és számokat láthatsz. A céltábla például azt mutatja, hogy mennyi pénzre van még szükség a cél eléréséhez, míg a malac a perselybe utoljára beosztott pénz mennyiségét mutatja. Kiadásnál a bevásárló kosár az utolsó kiadások összegét mutatja. Ha rákattintasz ezekre, egyből bekerül az összeg a megfelelő helyre, így nem kell feleslegesen gépelni.</p>

<p>A legnagyobb probléma általában az szokott lenni, hogy ha például a Lidl-ben vásároltál ételt, de betettél egy metszőollót és egy autó ablaktörlő folyadékot is a kosárba, akkor elő kell keresni a számlát és tételesen szétosztani a költségeket étel/kert/autó kategóriák között.</p>

<p>Hogy ne kelljen külön számológépet használni, a + - * műveleteket is tudod használni, a program pedig az Összeg mező jobb felső sarkában mutatja az eredményt.</p>

<x-figure name="koltsegvetes-kalkulator" title="Használhatsz + - * műveleteket és zárójelezést" />

<p>Az igazság az, hogy csak rajtad múlik hogyan használod a programot. Ha nem akarsz ilyesmivel pepecselni, legyen egy sima vásárlás kategóriád, és amikor ilyen boltokba mész, mindegy mit veszel, használd ezt a perselyt, és csak akkor rögzítsd külön kategóriába a terméket, ha ez tényleg fontos, vagy nagy összegű. A kérdés mindig az, hogy "Mi a célod?". <x-a href="{{ route('knowledge.show', 'mi-a-celod') }}" target="_blank">Erről bővebben itt tudsz olvasni.</x-a></p>

