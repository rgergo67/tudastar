<a class="anchor" id="2-1"></a>
<h3>Nullbázisú költségvetés</h3>

<p>Amit a Beosztom.hu használ, azt úgy hívják: <x-a href="{{ route('knowledge.show', 'nullbazisu-koltsegvetes-tervezes') }}" target="_blank">nullbázisú költségvetés tervezés</x-a>. Ez a régen is használt borítékos módszer tudományos neve, röviden így néz ki:</p>

<p>Fogsz egy csomó borítékot, befőttes üveget vagy malac perselyt <em>(mi ezt használjuk)</em>, és felcímkézed őket attól függően, hogy milyen kiadásiad szoktak lenni, vagy mikre szeretnél gyűjteni, majd szétosztod közöttük a pénzed az utolsó forintig.</p>

<x-figure name="persely-megtakaritas-auto-szorakozas" title="Beosztod a pénzed az utolsó forintig" width="1300" height="650" />

<p>Ha kiadásod lesz, előveszed a megfelelő perselyt, és a kiadás fajtájától függően cselekszel.</p>

<ul>
    <li><strong>Fix kiadás:</strong> Biztos lehetsz benne, hogy van rá pénzed, hisz előre félretettél már ennyit. <strong>Kiveszed a pénzt, és fizetsz.</strong></li>
    <li><strong>Változó kiadás:</strong> Hobbi, könyv, cukrászda: nem kell attól tartanod, hogy túlköltekezel, hisz annyit tudsz költeni, amennyit ezekre félretettél. Ha mégis többet akarsz költeni mint amennyi benne van, <strong>elgondolkozol azon, hogy honnan lehetne pénzt átcsoportosítani.</strong></li>
    <li><strong>Váratlan kiadás:</strong> Az első dolgod egy vésztartalék létrehozása lesz, így a váratlan kiadások sem lesznek többek bosszantó kellemetlenségnél, hisz <strong>lesz miből kifizetni.</strong></li>
</ul>

<p>Ezt követően minden alkalommal amikor bevételed van, egyből szétosztod a perselyek között, így minden forintodnak adsz valami <em>feladatot</em>.</p>

<a class="anchor" id="2-2"></a>
<h3>Talált pénz</h3>

<p>Gondolj csak bele: ha <strong>találsz az utcán 20.000 Ft-ot</strong>, mit teszl? Egyből elköltöd? Esetleg beteszed a pénztárcádba, aztán majd lesz valami?</p>

<p>Ha Beosztom.hu-t használsz, először rögzíted, hogy találtál ennyi pénzt, este pedig végigpörgeted a perselyeid listáját, és eldöntöd, hogy hogyan osztod szét. 5.000 Ft hitel törlesztés, 5.000 Vésztartalék, 5.000 Nyugdíj, 5.000 Szórakozás, csak rajtad múlik.</p>

<p>Az ilyen bevételek nem vesznek el a bankszámlán, hanem tudatosan megtervezheted a sorsukat. Tovább megyek, amikor van egy kis <em>felesleges</em> pénzed, annak beosztása kiváló önismereti feladat.</p>

<p>Régebben soha nem gondolkodtam el rajta, hogy mi a fontosabb, a gyerek játéka, a kutyaiskola, vagy a netflix előfizetés. Most, ha van egy kis plusz pénzem, mindig jól megfontolom, és ettől jobban érzem magam.</p>

<a class="anchor" id="2-3"></a>
<h3>Ha túlköltekezel</h3>

<p>Egy haditerv sem tökéletes, de attól lesz egy tábornok zseniális, hogy nem hasraütésszerűen csoportosítja át a csapatokat, hanem mérlegel. Neked is ezt kell tenned!

<p>Ha a szórakozás kategóriában már csak 1.000 Ft van, de az ismerősök nagyon el akarnak csábítani egy állatkerti sétára, amihez tuti társul még egy cukrászda is, és legalább 10.000 Ft-ot ott fogsz hagyni, nem probléma!</p>

<p>Nézd meg a többi perselyed, és döntsd el, minek a terhére fogsz szórakozni. Jobb esetben nem a gyerek játszós pénzét, vagy az ételre, nyugdíjra, vész esetére félretett pénzt fogod megkurtítani... Ha mégis, hát legalább akkor is tudod, hogy saját akaratodból tetted ezt, nem pedig véletlenül vagy tudatlanságból.</p>

<p>Megéri ennyit vacakolni? <strong>Feltétlenül!</strong> Ha rendbe akarod tenni a pénzügyeidet, vagy nyugodtan szeretnél költekezni, tudván, hogy az adott pénz tényleg erre lett szánva, és amire muszáj, arra még mindig marad, akkor nélkülözhetetlen a pénzügyi tudatosság. Egy kis gondolkodást és tervezést igényel, de megéri vele foglalkozni.</p>

<a class="anchor" id="2-4"></a>
<h3>Vésztartalék</h3>

<p>Az egyik legfontosabb kategória a vésztartalék lesz <em>(nálunk a perselyeket kategóriákba lehet rendezni)</em>. Ide minden hónapban csepegtetsz egy kis pénzt egészen addig, amíg az ott lévő összeget látva biztonságban nem érzed magad.</p>

<p>Lehet egy darab Vésztartalék nevű perselyed, vagy egy teljes kategóriád tele vésztartalék perselyekkel (autó, ház, egészségügy). A lényeg, hogy <strong>csinálj legalább egyet</strong>, és próbáld meg megoldani, hogy minden hónapban kerüljön bele pénz. Akkor is, ha csak 1.000 Ft!</p>

<x-figure name="vesztartalek-szef" title="6 havi fizetés a biztonsági vésztartalék" width="327" height="500" />

<p>Azt szokták mondani, hogy 6 havi fizetésednek megfelelő vésztartalékot érdemes képezni, így ha keresőképtelenné válsz, tiszta fejjel, higgadtan tudsz új állás után nézni.</p>

