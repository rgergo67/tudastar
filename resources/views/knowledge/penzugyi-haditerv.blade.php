<a class="anchor" id="1"></a>
<h2>A háború, amit vívsz</h2>

<p>Ahhoz, hogy megértsd miért van szükség haditervre, mutatunk egy rövid hasonlatot, talán már a legelején segít megérteni a lényeget. Képzeld el a következő helyzetet:</p>

<blockquote>
    <div class="mt-8 lg:grid lg:grid-cols-3 lg:gap-8">
        <div class="mt-8 lg:mt-0 lg:col-span-2">
            <p>Tábornok vagy, hatalmas népességű országod évek óta háborúban áll az ellenséggel. A taktikád a következő: minden hónapban besorozol 200.000 újoncot, és harcba is küldöd őket mind egy szálig.</p>

            <p>Vannak határszakaszok, ahol rendszeres támad az ellenség, ennek ellenére mindenkit a fővárosban állomásoztatsz, és csak a támadás pillanatában küldöd ki a csapatokat. Időről időre jön egy 50-100.000 fős váratlan támadás is, ilyenkor csak bízni tudsz benne, hogy marad elég katona a kivédésére.</p>

            <p>Nincs haditerved, csak egy halom katonád, és a remény, hogy következő hónapban is tudsz majd toborozni.</p>

            <p>Éjjelente nem alszol. Mi van, ha következő hónaptól nem tudsz toborozni? Ha az ellenség akkor támad, amikor már alig van embered? Hogy indítasz támadást, ha még magadat sem tudod megvédeni?</p>

            <p><strong>Szükséged van egy jó haditervre.</strong></p>
        </div>
        <div class="relative lg:row-start-1 lg:col-start-3">
