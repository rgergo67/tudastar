<p>A fentieket állítsd szembe ezzel:</p>

<p>Minden hónapban kapsz 200.000 Ft fizetést, amit el is költesz az utolsó forintig.</p>

<p>Vannak ugyan fix kiadásaid, de nem különítesz el rájuk pénzt, helyette csupán egy darab összeget látsz a bankszámládon. Néha vannak nagyobb, váratlan kiadásaid, például elromlik az autó, vagy csőtörés lesz, ilyenkor csak remélni tudod, hogy elég lesz a pénzed a következő fizetésig.</p>

<p>Éjjelente nem alszol. Mi van, ha holnap elveszíted az állásod? Ha jön egy váratlan kiadás? Ha a saját és családod megélhetését is alig tudod biztosítani, hogyan valósítod meg az álmaidat?</p>

<hr>

<p>Csak a legégetőbb problémákat próbáltuk bemutatni <em>(költségvetés és vésztartalék hiánya)</em>, ugyanilyen fontos az impulzus vásárlás, a hitel, a függőségek kezelése, pénzügyi bizonytalanság, közös családi kassza vagy vállalkozás pénzének külön kezelése.</p>

