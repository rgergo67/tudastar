<h2>A Beosztom.hu oldalról bővebben</h2>

<p>A Beosztom.hu azért készült, mert volt egy problémánk: nem tudtuk, hogy a meglévő pénzünkből mire mennyit költhetünk.</p>

<p>Ez több esetben is előjött:</p>
<ol>
    <li>Született egy fiunk, és nincs az a pénz, amit ne tudnánk elkölteni gyerek cuccokra, játékokra vagy ruhára...</li>
    <li>... de mi magunk is nagy társasjáték és könyv rajongók vagyunk, ha valami felkelti az érdeklődésünket, impulzus vásárlóként azonnal megvesszük. Hol a határ?</li>
    <li>Van egy KATA vállalkozásunk aminek a pénze a személyes bankszámlánkra megy, így összekeveredik a vállalkozás pénze a sajátunkkal.</li>
</ol></p>

<p>Ugyan nem hónapról hónapra élünk, mégis észrevettük, hogy feltűnően egy helyben áll, sőt egyre többször csökken a megtakarított pénzünk mennyisége. Tudtuk, hogy nagy szerepet játszik ebben a fent említett három dolog, de nehéz volt megmondani, hogy a meglévő pénzünkből mennyi az amit "elherdálhatunk", és mennyi az, aminek igazából lenne más feladata.</p>

<p>Mindenközben végig az járt a fejünkben, hogy mi lesz, ha COVID közben valamelyikőnk komolyabban megbetegszik? Vagy ha megszűnik a munkahely?</p>

<p>Némi keresgélés után találtunk kiadáskövető alkalmazásokat, azonban ez a megoldás nekünk nem jött be: utólag tudjuk nézegetni, hogy mire mennyit költöttünk. Nekünk ehelyett arra van szükségünk, hogy előre be tudjuk osztani a pénzünket, és biztosan legyen vésztartalékunk is.</p>

<p>Találtunk költségvetés készítő alkalmazást is, de vagy külföldi volt, vagy drága, vagy bonyolult. Megcsináltuk tehát azt, ami a saját igényeinket kielégítette, és ezt tettük elérhetővé a Beosztom.hu címen.</p>

