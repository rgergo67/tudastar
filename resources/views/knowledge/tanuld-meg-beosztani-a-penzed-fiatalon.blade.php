<h1>Tanuld meg beosztani a pénzed fiatalon</h1>

<p>Fiatal vagy. Vagy támogatnak szüleid, vagy magadnak kell megkeresned a tanulásra, kolira, kajára és szórakozásra valót, de az is lehet, hogy már teljesen a saját lábadon állsz. Valószínűleg nem keresel milliókat, talán még százezreket sem. Hogy jutsz így egyről a kettőre?</p>

<p>Amíg nem tudod növelni a bevételeidet, a kiadásokat kell csökkenteni. Nem muszáj lemondanod az összes szórakozásról, mindössze annyit kell tenned, hogy összeszámolod minden pénzed, felcimkézel néhány perselyt attól függően, hogy mire szánod a pénzt, és beosztod.</p>

<p>A probléma ott kezdődik, hogy a bankszámlán levő pénzt nehéz perselybe tenni. Erre nyújt megoldást a beosztom.hu a virtuális perselyekkel és kategóriákkal, de mielőtt belevágunk a legfontosabb: </p>

<p><strong>Nem akarunk lehúzni!</strong> Egy hónapig tudod használni az oldalt, mielőtt eldöntenéd, hogy megéri-e az árát. Ha nem jött be, nem kell semmit magyarázkodni, egyszerűen tovább állsz és kész, megértjük. Sőt még ekkor is azt javasoljuk, hogy tarts ki a pénzed beosztása mellett, használj erre jegyzetfüzetet vagy vezesd Google Docsban. A pénzügyi jólét egyik alapja az, hogy előre be tudd osztani a pénzed.</p>

<h2>Hogyan tudom beosztani a pénzem előre?</h2>

<p>Nagyon jó kérdés! A legtöbben már a legelején feladják, mondván <em>"Honnan tudhatnám, hogy hányszor fogok elmenni a hónapban moziba, kávézni, vagy mennyit költök majd kajára?!"</em> Sajnos ők rossz oldalról közelítik meg a kérdést, gondolj rá inkább így: <em>"Most kaptam pénzt. Ha a héten minden nap rávesznek a barátok a cukrászdára, moziba is elmegyünk és ruhát is veszek, akkor honnan fogom tudni, hogy hónap végén marad majd pénz kajára és albérletre?"</em> Ugye mennyire más innen nézve?</p>

<p>Vannak dolgok, amikre nem igazán van ráhatásod, vagy amin nem érdemes spórolni. Enni például muszáj. Sokan esnek abba a hibába, hogy egy hónapon keresztül követik az étel kiadásukat, és megrökönyödve tapasztalják, hogy 50-80.000 Ft elmegy rá, akkor ezen kell spórolni! Pedig az esetek többségében pont nem, az egészséges táplálkozás az egyik legfotnosabb dolog. Helyette kicsit utána kell olvasni a dolgoknak, és kiderül, hogy az 1500 Ft-os pizza árából több adag cézár salátát vagy gyümölcsös müzlit, vagy akár egy bulgurus zöldséges lazacot is el lehet készíteni.</p>

<p>De természetesen csak rajtad múlik, hogyan osztod be a pénzed. Ha úgy érzed az ételen tudsz spórolni, akkor tedd azt. A megmaradt pénzt pedig oszd be olyan helyre, ahol jobb helye van, kezdetben például képezz egy vésztartalékot, vagy kezd el visszafizetni a diákhiteled.</p>

<p>Ha teheted, mindig szánj egy kicsit szórakozásra, kikapcsolódásra, sportra különben nagyon nehéz tartani magad a tervekhez.</p>

<h2>Mi történik, ha kicsúszok a keretből?</h2>

<p>Semmi. Senki nem felügyel vagy ellenőriz, ez a te életed, a te döntéseid. Időnként mindenkivel megesik, hogy nem tudja tartani magát a tervekhez, vagy jön valami váratlan esemény ami felborítja a rendszert. </p>

<p>Szerencsére még ilyenkor is hasznát veszed a rendszernek, mert látod, hogy honnan tudsz átcsoportosítani.</p>

<p>Tegyük fel, hogy a szórakozás perselyedben már nincs pénz, de <em>Halott Pénz</em> koncert lesz az egyetemen, és úgy érzed erre mindenképpen el kell menned. Semmi gond, megnézed melyik perselyben mennyi pénz van, és úgy döntesz, hogy a kondi bérlet árát vagy az ehavi fodrászra tervezett pénzt áldozod fel. Ha már ez sincs, csak kaja meg albérlet pénz, és abból költesz... Nos, akkor legalább tudatosan hoztad meg a döntést, és nem fog váratlanul érni, hogy éhezni kell, vagy kölcsönkérni.</p>

