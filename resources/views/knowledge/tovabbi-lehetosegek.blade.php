<p>Rákattintva megjelenik egy keresési mező, mely a kategóriák és perselyek nevében keres. Ha keresés során a kategória alján lévő ...-ra kattintasz, az összes persely megjelenik abban a kategóriában.</p>

<p>Az is hasznos lehet, hogy azokat a kategóriákat és perselyeket helyezed előre, melyeket nap mint nap használsz.</p>

<h3>Törlés</h3>

<p>Ha már nincs szükséged egy kategóriára vagy perselyre, törölheted őket. Ekkor a persely(ek)ben lévő összeg automatikusan visszakerül a beosztható kupacba az oldal tetején.</p>

