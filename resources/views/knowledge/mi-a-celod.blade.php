<a class="anchor" id="honaprol-honapra"></a>
<h3>1. Hónapról hónapra élek</h3>

<p>Ilyen esetben a legfontosabb egy vésztartalék képzése, ami jó esetben a 6 havi fizetésed. Mennyi?!?! Igen, 6 havi fizetésed. Félre lehet ennyit tenni, csak kicsiben kell kezdeni. Erre az egyik legjobb megoldás a pénz előre történő beosztása.</p>

<p>Egyik pillanatról a másikra megváltozni, ha nem is lehetetlen, de nagyon nehéz. Hozd létre a Vésztartalék perselyt, állítsd be a 6 havi fizetést célösszegnek, és minden hónapban a fizetésed egy részét tedd bele. Ha csak 10.000 Ft-ot, akkor annyit, és bármi történik, ne nyúlj hozzá, ez a pénz innentől nem létezik.</p>

<p>Persze fizikailag ott van a zsebedben, vagy a bankszámládon és nagyon csábító... Éppen ezért, ha van lehetőséged, akkor ennél az egy perselynél próbáld meg valahogy ténylegesen is elkülöníteni a többi pénzedtől. Vagy tedd ténylegesen egy perselybe, vagy nyiss egy számlát az államkincstárnál és vegyél belőle állampapírt.</p>

<a class="anchor" id="nem-tudom-novelni-a-megtakaritasaimat"></a>
<h3>2. Nem tudom növelni a megtakarításaimat</h3>

<p>Hiába van 1.000.000 Ft a bankszámlámon, legyen akármennyi a fizetésem, a fejemben ott az 1 milliós határ, és valahogy minden hónap végén itt kötök ki. Ha nálad is hasonló a helyzet, valószínűleg tudunk segíteni.</p>

<p>Spórolni csak tudatosan lehet. Ez azt jelenti, hogy annak a pénznek amit nem teszel félre, annak az eddigi szokásaid miatt egyszerűen lába fog kélni. Adj feladatot minden egyes forintnak előre! Gondold végig a hónapot, oszd be először a fix és nélkülözhetetlen költségeket, aztán a maradékot ne az "Akármire" perselybe tedd, hanem legalább a harmadát szánd egy hosszútávú célra.</p>

<p>Dehát ez olyan banálisan egyszerű... mondhatnád, és igazad is lenne. A legtöbb probléma a szokásainkból ered, a nemtörődömségünkből, vagy egyszerűen abból, hogy nem tervezünk. Mi éppen ezen próbálunk segíteni: váljon szokásoddá, hogy odafigyelsz a pénzedre, és előre feladatot adsz minden egyes forintnak.</p>

<a class="anchor" id="van-egy-fuggosegem"></a>
<h3>3. Van egy függőségem</h3>

<p>Ahogyan a legtöbbünknek. És itt nem kell feltétlenül az alkoholizmusra és dohányzásra gondolni, ugyanúgy függőség az, amikor</p>

<ul>
    <li>azonnal meg kell venned valamit, ha megtetszik <i>(impulzus vásárlás)</i></li>
    <li>nem tudsz létezni műkörmös, fodrász, kozmetikus nélkül</li>
    <li>az autód meghal, ha nem mosod le minden nap nano protect programmal</li>
</ul>

<p>Nem akarunk "kigyógyítani" a függőségedből, épp elég nagy eredmény az is, ha első lépésként felismered, hogy problémád van. Akinek függősége van, az általában a pénzügyein is észreveszi ezt, például hónapról hónapra él, vagy nem tudja növelni a megtakarításait.</p>

<blockquote>
    <p>Nekünk például könyv és társasjáték függőségünk van, impulzus vásárlók vagyunk: ha egy nagyon megtetszik, azonnal megvesszük. Emellett azzal a problémával is küzdöttünk eddig, hogy nem tudtuk növelni a megtakarításainkat, így ez is jelezte, hogy valószínűleg van egy rossz szokásunk vagy függőségünk.</p>
</blockquote>

<p>Nagyon fontos, hogy mi a függőséged kordában tartásában tudunk csak segíteni, annak megszüntetésében nem. Ha előre meghatározod mennyit költhetsz egy hónapban cigire vagy impulzus vásárlásra, és ezt a szokásos havi költésed alá lövöd be, akkor havonta legalább egyszer szembesülsz a függőségeddel: amikor konkrétan elfogy a függőségedre beosztott pénzed.</p>

<p>Ilyenkor két dolgot tehetsz: vagy azt mondod, hogy ez a pénzbeosztás hülyeség és törlöd a fiókod, vagy sóhajtasz egyet, és egy másik perselyből teszel át még pénzt ebbe. Mert eleinte valószínűleg semmi nem fog változni.</p>

<p>Nekünk 3-4 hónap kellett ahhoz, hogy végre megértsük és lássuk azt, hogy a függőségünk konkrétan honnan viszi el a pénzt. Ha minden be van osztva, konkrét döntést kell hozni: a gyereknek a játék pénzéből költsek, vagy az egészségügy perselybe félretett pénzből...</p>

<a class="anchor" id="nem-tudom-mennyit-kolthetek"></a>
<h3>4. Nem tudom, hogy mennyit költhetek</h3>

<p>Van megtakarításod, a fizetésed is elfogadható, viszont mikor vásárolnál valami komolyabb dolgot, fogalmad sincs, hogyan fog ez téged érinteni anyagilag <i>(nyilván annyival kevesebb pénzed lesz, mint amennyit elköltesz, de hosszú távon ez mennyire fog fájni?).</i> Ez nagyon gyakori probléma, mivel aki nem hónapról hónapra él, az gyakran nincs is teljesen tisztában az éppen aktuális pénzügyi helyzetével. </p>

<p>Minek számolgatni, ha látod, hogy van 3 millió a bankszámlán? Persze, hogy kijön belőle egy 400 ezres TV karácsonyra. És akkor már anyának is egy 200 ezres kávéfőző, a nagy fiúnak egy 350 ezres számítógép, a lánynak egy kiskutya.</p>

<p>Persze ez egy lesarkított példa (vagy nem?), lehet érezni, hogy 3 millióból 1 millió azért nem kevés, ráadásul nem számoltuk bele, hogy egy kutya fenntartásának mennyi az éves költsége, mennyit tegyünk félre orvosi kezelésre, kutyaiskolára, játékokra stb.</p>

<p>Legyen félretéve a pénz a fontos dolgokra (vésztartalék, egészség, kaja), legyen megtakarításos perselyed (nyugdíj, gyereknek oktatás), illetve legyenek olyan perselyeid, mint nyaralás, ajándék, szórakozás. Utóbbiakba a "felesleges" pénzed teheted, így amikor jön a karácsony, vagy a nyaralós szezon, egyszerűbb eldönteni, hogy itthon menjetek valahova, vagy a tengert is megengedhetitek magatoknak.</p>

<a class="anchor" id="a-vallalkozasom-penze-osszefolyik-a-sajatommal"></a>
<h3>5. A vállalkozásom pénze összefolyik a sajátommal</h3>

<p>Aki KATA vállalkozó, és a saját személyes bankszámláját használja a vállalkozásához is, az belefuthat ugyanabba a problémába, mint mi: nehéz megmondani, hogy meddig tart a vállalkozás pénze, és hol kezdődik a sajátunk. Főleg akkor, ha mellette főállásban is dolgozunk valahol.</p>

<p>Keresel mondjuk havi nettó 200.000-et a főállásodban, a KATA vállalkozásod pedig kézműves tárgyak készítésével foglalkozik. Változó, hogy havonta mennyi megrendelésed van, néha semmi, karácsony körül pedig akár 150-180.000 Ft is.</p>

<p>De mint tudjuk, minden vállalkozásnak vannak költségei. KATA-sként ez például legalább évi 350-400.000 Ft (bevételtől függetlenül), és ezek még csak az adók és könyvelő. Nincs benne az alapanyag, a bérleti díjak, reklám és még sorolhatnánk.</p>

<p><b>Biztos vagy benne, hogy a vállalkozásod jövedelmező?</b> Vagy lehet, hogy nem az, de nem tűnik fel, mert a főállásodból kiegészíted, csak még nem tűnt fel... Mi pont ebbe futottunk bele, sok felesleges dolgot lenyirbáltunk a vállalkozás költségeiből, hogy fenn tudja tartani saját magát.</p>

<p>A Beosztom.hu felületén csináltunk egy Vállalkozás kategóriát, abban van egy Fix költségek persely a KATA adóknak, egy Marketing, egy Befektetés, egy Szerverek és egy Alapanyagok persely. Amikor a vállalkozásnak bevétele keletkezik, először a fix költségeket töltjük fel, majd szerverek, alapanyagok, végül marketing és befektetés. Megyünk a nélkülözhetetlentől az opcionális felé.</p>

<a class="anchor" id="rendbe-akarom-tenni-a-csaladi-kasszat"></a>
<h3>Rendbe akarom tenni a családi kasszát</h3>

<p>A családi kassza sokkal bonyolultabb, mint mikor egyedül éltél, vagy mint mikor pároddal külön kasszán voltatok. Nem lehet már csak úgy felelőtlenül költekezni. Amikor veszel magadnak egy új ruhát, vagy a héten harmadszor viszed el lemosatni a kocsit, a közösből veszel el, ennyivel kevesebb jut a párodnak, a gyermekednek.</p>

<p>Attól jó egy családi kassza, hogy előre, egyértelműen be van osztva a pénz. Persze bármikor leülhettek és áttervezhetitek, de sok konfliktust és meglepetést el lehet kerülni azáltal, hogy világosak a célok, és hogy mire mennyi pénz van félretéve.</p>

<p>A Beosztom.hu oldalon nincs külön családi fiók. Ha pároddal közös kasszán vagytok, azt javasoljuk, hogy valamelyikőtök regisztráljon be, és mindketten ugyanazzal a fiókkal használjátok az oldalt.</p>

