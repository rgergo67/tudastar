<p>Némi magyarázat az összegekhez, talán segít a tervezésben:</p>

<ul>
    <li><b>Gyógyszerek, kezelések</b> - ha baj történik, akkor ne azzal kelljen foglalkozni, hogy miből fizetünk ki egy műtétet, hogy mennyibe kerül a kezelés, vagy hogy elmehetünk-e magánba ahelyett, hogy 3 hónapot várunk az állami időpontra.</li>
    <li><b>Megelőzés</b> - Az egészség megőrzéséhez fontos a lazítás, stressz csökkentése, helyes táplálkozás. Innen lehet költeni masszázsra, gyógyfürdőre, vitaminokra, túrázásra.</li>
    <li><b>Jótékonyság</b> - Ha azt szeretnénk, hogy a világ jobb hely legyen, nekünk is ki kell vennünk a részünket jobbátételében. Nagyon jó cikk olvasható erről a <x-a href="https://kiszamolo.hu/az-adakozasrol-meg-egyszer/" target="_blank">kiszamolo.hu oldalon</x-a>.</li>
    <li><b>Vállalkozás</b> - Itt egy évre előre félre lettek téve a havi fix költségek, így ha nincs bevétel, az sem jelent akkora problémát. Akinek KATA vállalkozása van és a személyes bankszámláját használja hozzá, annak külön segítség, hogy a vállalkozói és magán vagyon ilyen módon elkülöníthető.</li>
    <li><b>Vésztartalék 6 havi</b> - Először 1, majd 2 havi fizetést érdemes beállítani célként, egészen addig, amíg el nem jutsz a 6 havi fizetéshez. Ha ennyi pénz félre van téve (akár állampapírba), akkor rengeteg stressztől óvod meg magad. Ha váratlanul elbocsájtanak, vagy 1-2 hónapra te vagy egy hozzátartozód 24 órás ápolásra szorul, akkor sem azon kell aggódni, hogy miből veszel ételt, fizetsz albérletet.</li>
    <li><b>Diákhitel</b> - Ez a persely valószínűleg rossz kategóriába került, de akármikor lehet csinálni egy Hitel kategóiát, és áthúzni bele. Akkor van értelme létrehozni egy ilyen perselyt, ha nem a fizetésed beérkezése után egyből utalod a törlesztőt, hanem később. Így biztos lehetsz benne, hogy véletlenül sem költöd el másra.</li>
    <li><b>Közlekedés</b> - Itt is 1 évre előre tesszük félre a pénzt (ha van rá módunk). Főleg a szervizek szoktak keményen érinteni minket, mert ha egyszer egy autó elromlik, akkor egyszerre romlik el rajta minden...</li>
    <li><b>Számlák</b> - Szét is bonthatod áram/víz/gáz/hulladék/stb kategóriákra, vagy félretehetsz egyben egy 3-4 hónapos átlagot rájuk, és mindet ebből vonogatod.</li>
    <li><b>Bármire</b> - Amikor hirtelen nem tudjuk, hogy valamit hova soroljunk, akkor azt innen vonjuk le.</li>
    <li><b>Kaja</b> - Ide a Lidl, Aldi és hasonló bevásárlások tartoznak, általában nem szoktuk tételesen megnézni, hogy minden kaja-e, az esetek többségében az. Néhány havi vásárlás költség van félretéve, egy kis plusz ráhagyással, biztos ami biztos. </li>
    <li><b>Jövő</b> - A gyereknek 18 éves koráig lehet babakötvénybe pénzt tenni, azt itt vezetjük.</li>
</ul>

<p>Ezek mellett még lehet nyugdíjra, hitelre, oktatásra és ezer más dologra pénzt félretenni. Ha nem bonyolítod túl, egyszerűbb lesz használni a mindennapokban.</p>

