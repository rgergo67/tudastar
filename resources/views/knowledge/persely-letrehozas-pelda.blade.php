<h2>Persely létrehozás példa</h2>

<p>Nincs ökölszabály vagy olyan megoldás, ami mindenkinek tökéletes lenne. Ettől függetlenül mutatunk egy példát, ami alapján létrehozhatod kategóriáidat és perselyeidet.</p>

<h3>Első példa</h3>

<p class="italic">Ha nem állsz túl jól anyagilag, kérjük ne botránkoztassanak meg a számok, ez csak egy példa. Azt javasoljuk, hogy a lehetőségeidhez és terveidhez képest állítsd be a perselyek cél összegeit.</p>

