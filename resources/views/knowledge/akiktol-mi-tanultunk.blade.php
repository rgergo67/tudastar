<h2>Akiktől mi tanultunk</h2>

<p>Sok jó magyar pénzügyi tanácsadó van, ezek közül hármat emelnénk ki, mi is tőlük tanul(t)unk. Egyikkel sem állunk üzleti kapcsolatban, azért ajánljuk őket, mert meg vagyunk elégedve azzal, amit olvasunk tőlük.</p>

<h3><x-a href="https://kiszamolo.hu/a-blog-cikkei-osszegyujtve/" target="_blank">Kiszámoló</x-a></h3>

<p>Miklós pénzügyi tanácsadással foglalkozik, nagyon jó anyagai vannak, melyeket <x-a href="https://kiszamolo.hu/a-blog-cikkei-osszegyujtve/" target="_blank">itt találsz témákba csoportosítva</x-a>. Nem csak pénzügyekkel, hanem életvitellel és gondolkodásmóddal kapcsolatos írásai is kiválóak, ha van időd, én mindenképpen javaslom azokat is elolvasásra.</p>

<p>Ha nem szeretsz olvasni, <x-a href="https://www.youtube.com/watch?v=vgnlSwmUdQA" target="_blank">youtubon elérhető a Kiszámoló Akadémia</x-a>, szintén nagyon jó. Készülj fel rá, hogy Miklósnak van egy bizonyos stílusa, ami nem mindenkinek szimpatikus. Ha olyant mond ami úgy érzed neked fáj, vagy sértő, akkor gondolkodj el azon, hogy valószínűleg rátapintott egy problémádra, amivel érdemes foglalkozni.</p>

