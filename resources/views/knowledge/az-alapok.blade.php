<h2>Az alapok</h2>

<p>Elöljáróban annyit, hogy nem vagyunk pénzügyi szakemberek. Elolvastunk sok-sok cikket a témában, és az ezekből leszűrt "lényeget" osztjuk itt meg. Ha mélyebben bele szeretnéd magad ásni, <x-a href="{{ route('knowledge.show', 'akiktol-mi-tanultunk') }}" target="_blank">akkor itt találod az általunk javasolt oldalakat</x-a>.</p>

<h3>Adj munkát minden forintnak</h3>

<p>Ha bevételt szerzel, döntsd el előre, hogy mire fogod használni, legyen szó fizetésről, pénz visszatérítésről, vagy az utcán talált 5.000 Ft-ról.</p>

<p>Vegyük példának az utolsót: találsz 5.000 Ft-ot, beleteszed a pénztárcádba. Szerinted ennek az "ingyen" pénznek meg fogod-e takarítani bármelyik forintját, vagy kegyetlenül a seggére versz az egésznek és elköltöd? Mert nekem erre az a válaszom, hogy fogalmam sincs. Ez a pénz bekerül a nagy gyűjtőbe, és onnantól nyoma vész.</p>

<p>De! Ha megtalálás után felváltom, és 1.000 Ft-ot beleteszek egy megtakarítás perselybe, 2.000 Ft-ot egy új autó perselybe, 2.000 Ft-ot pedig egy szórakozás perselybe, akkor máris feladatot adtam minden forintnak.</p>

<x-figure name="persely-megtakaritas-auto-szorakozas" title="Megtakarítás, autó, szórakozás persely" width="1300" height="650" />

<p>Ha nem készpénzről van szó, hanem például az állam utal neked CSED-et, vagy a nagyitól kapsz zsebpénzt a bankszámládra, azt nehéz malacperselybe tenni, illetve folyamatosan újraszámolni, hogy melyikben mennyi van. A Beosztom.hu ezen segít a felturbózott virtuális perselyekkel és kategóriákkal.</p>

<p>Az egész lényege viszont az, hogy mindig minden forintodnak adj feladatot, és oszd be valamelyik perselyedbe.</p>

<h3>Ha túlköltekezel egy perselyben</h3>

<p>Ez nem a szentírás. Ha a szórakozás kategóriában már csak 1.000 Ft van, de az ismerősök nagyon el akarnak csábítani egy állatkerti sétára, amihez tuti társul még egy cukrászda is, és legalább 10.000 Ft-ot ott fogsz hagyni, nem probléma!</p>

<p>Nézd meg a többi perselyed, és döntsd el, minek a terhére fogsz szórakozni. Jobb esetben nem a gyerek játszós pénzét, vagy az ételre, nyugdíjra, vész esetére félretett pénzt fogod megkurtítani... Ha mégis, hát legalább akkor is tudod, hogy saját akaratodból tetted ezt, nem pedig véletlenül vagy tudatlanságból.</p>

<p>Mi értelme ennyit vacakolni? Ha rendbe akarod tenni a pénzügyeidet, vagy nyugodtan szeretnél költekezni, tudván hogy az adott pénz tényleg erre lett szánva, akkor nélkülözhetetlen a pénzügyi tudatosság. Erőfeszítéssel jár, de megéri vele foglalkozni.</p>

<h3>Vésztartalék</h3>

<p>Van azonban olyan persely, amiből jobb, ha nem veszel ki pénzt kényed kedved szerint, ez pedig a vésztartalék.</p>

<p>Lehet egy darab Vésztartalék nevű perselyed, vagy egy teljes kategóriád tele vésztartalék perselyekkel (autó, ház, egészségügy). A lényeg, hogy <strong>csinálj legalább egyet</strong>, és próbáld meg megoldani, hogy minden hónapban kerüljön bele pénz. Akkor is, ha csak 1.000 Ft!</p>

<x-figure name="vesztartalek-szef" title="6 havi fizetés a biztonsági vésztartalék" width="327" height="500" />

<p>Azt szokták mondani, hogy 6 havi fizetésednek megfelelő vésztartalékot érdemes képezni, így ha keresőképtelenné válsz, tiszta fejjel, higgadtan tudsz új állás után nézni.</p>

<h3>Hosszútávú megtakarítások</h3>

<p>Ilyen lehet például a nyugdíj, gyereknek egyetem vagy lakás, autó vásárlás... Minél előbb kezdesz el rájuk spórolni, annál jobb. És megint csak: még akkor is tegyél félre, ha csak pár ezer forintot tudsz nélkülözni.</p>

<p>Mi személy szerint infláció követő állampapírba tesszük a nyugdíjra szánt pénzünket <em>(egyáltalán nem vagyunk benne biztosak, hogy 30-40 év múlva még lesz nyugdíj)</em>, és babakötvénybe a gyereknek szánt összeget. Tudjuk, hogy hosszú távon részvényekkel is jól/jobban lehet járni, de ahogy írtuk, nem vagyunk pénzügyi szakemberek, mi ezt találtuk biztonságosnak.</p>

<p>Az biztos, hogy semmiképpen nem érig meg 40 évig a párna alatt tartani a pénzt. <x-a href="https://www.ksh.hu/docs/hun/xstadat/xstadat_hosszu/h_qli001.html" target="_blank">Itt például</x-a> megnézheted, hogy 1979-ben a havi átlag fizetés 3.877 Ft volt, míg 40 évvel később, 2019-ben 367.833 Ft. Ha valaki 6 havi fizetését párnahuzatban tartotta volna ez idő alatt, az most tudna belőle venni egy porszívót.</p>

<h3>Adósság visszafizetése</h3>

<p>A mai világban, sokszor elkerülhetetlennek tűnik a hitel felvétele, legyen szó akár lakás vásárlásról, akár egy váratlan esemény miatti hirtelen kiadásról. Amíg a hitelt nem törlesztetted vissza, minden hónapban húzni fogja a zsebed az összege, megnehezítve a céljaid elérését.</p>

<p>Mindenképpen azt javasoljuk, hogy először képezz egy kisebb (1-2 havi fizetés) vésztartalékot, hogy ha beüt a krach, ne kelljen újabb hitelt felvenned.</p>

<p>Ezt követően, amennyiben több hitellel is rendelkezel, a következő megoldást érdemes alkalmazni:</p>

<ul>
    <li>Rendezd a hiteleid növekvő sorrendbe kamatozás alapján</li>
    <li>Minden hiteledre a minimum törlesztőt fizesd, kivéve a legnagyobb kamatozásút</li>
    <li>Erre a lehető legtöbbet fizesd, amit nélkülözni tudsz a mindennapjaidból</li>
    <li>Ha sikerült visszafizetned, ugorj a következő legnagyobb kamatozásúra</li>
</ul>

