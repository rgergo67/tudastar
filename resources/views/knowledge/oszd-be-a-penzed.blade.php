<p>Az automatikus beosztás először azokat a perselyeket tölti meg amik fontosnak vannak jelölve, aztán azokat amiknek közelebb van a határideje, végül azokat amiknél a cél eléréséhez kevesebb pénz kell. A program által javasolt beosztást tetszés szerint módosíthatod, mielőtt a pipával véglegesítenéd.</p>

