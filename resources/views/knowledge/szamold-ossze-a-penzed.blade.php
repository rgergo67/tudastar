<h2>1. Számold össze a pénzed</h2>

<p>Ha rendbe szeretnéd tenni a pénzügyeidet, az egyik legfontosabb dolog, hogy tisztában legyél az aktuális helyzeteddel: tudd, hogy itt és most mekkora összeggel rendelkezel.</p>

<p>Az csak rajtad áll, hogy ebbe mit számolsz bele. Mi például ide vesszük a készpénzt, a bankszámlán és egészségpénztárban lévő pénzünket, és az állampapírt. Nem számoljuk ide a részvényeinket, aranyban és ezüstben lévő befeketésünket, vagy az ingatlanunkat, autót, holott ezek is mind pénzzé tehetők.</p>

<blockquote>Mi azért csináljuk így, mert előbbiekhez vagy könnyebb hozzáférni, vagy viszonylag könnyű megmondani, hogy x év múlva mennyit fognak érni. Ha szeretnéd, te nyugodtan számold át utóbbiakat is forintra és oszd be, csak ne felejtsd el időnként újraszámolni és korrigálni az összegeket.
</blockquote>

<p>Ha sikerült mindent összeadni, akkor belépés után kattints az oldal tetején található <span class="text-green-600 font-bold">Minden beosztva</span> feliratra, és írd be az összeget.</p>

<x-figure name="beosztas" title="Először kattints a szövegre vagy ceruzára, majd a megjelenő mezőbe írd be az összeget és egy megjegyzést (nem kötelező), végül üss ENTER-t vagy kattints az egyenlőség jelre." width="419" height="201" />

<p>Ha esetleg elrtontottad és javítani kell, negatív összeget is beírhatsz. Írhatsz megjegyzést is, de ez nem kötelező.</p>

<p>A fenti képen látható varázspálca a pénz automatikus beosztásában segít. Ezt akkor tudod használni, ha már beállítottál néhány célt, erről egy kicsit később írunk. Előbb nézzük meg mire valók a kategóriák és a perselyek.</p>

