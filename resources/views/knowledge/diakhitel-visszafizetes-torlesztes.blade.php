<h2>Diákhitel visszafizetés, törlesztés</h2>

<p>A diákhitel felvétele egyszerű, visszafizetése és törlesztése azonban annál nehezebb. A tanulmányok végére több milliós tartozást is össze lehet gyűjteni:</p>

<p>Jelenleg félévente 750.000 Ft Diákhitel 1-et lehet max felvenni. Ha élvezed az egyetemet és 6 helyett 11 félév alatt végzel, több mint 8 millió Ft tartozásod lesz. És ha ezt a szabad felhasználásút felvetted, valószínáleg a Diákhitel 2-t is fel fogod a képzés költségére, ami megint milliós tétel lesz (bár egyelőre az nem kamatozó).</p>

<p>Természetesen tudjuk, hogy van akinek nincs más lehetősége, mint a hitel felvétele. Ha felvetted, felvetted. Ha sokat, akkor sokat. Mi először csak a nagyságrendeket szeretnénk szemléltetni, és hogy miért érdemes minél hamarabb, minél többet visszafizetni, törleszteni.</p>

<p>Maradjunk annál a példánál, hogy 11 félévig tanulsz, minden hónapban felveszel 150.000 Ft-ot, a végére pedig lesz 8.250.000 Ft tartozásod. Ekkor így néz ki a kötelező diákhitel törlesztés, ennél havonta csak többet fizethetsz (ha megengedheted magadnak):</p>

