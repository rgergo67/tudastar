<p>35 évig havi 30.000 Ft-ot fizetsz, ha 500.000 Ft-os bruttó fizetésed van. Ez kb 330.000 nettó, tehát ennyit kapnál kézhez, ebből 30.000 Ft diákhitel törlesztés. Talán nem tűnik soknak, néhány gondolat ezzel kapcsolatban:</p>

<p>Nem mindenki kap egyetem elvégzése után ilyen, vagy ennél jobban fizető állást. Ha kevesebbet keresel, ugyan kevesebbet is kell visszafizetned havonta, ám annál tovább. <x-a href="https://diakhitel.hu/diakhitel1" target="_blank">Próbáld ki a kalkulátort</x-a>, megtalálod-e hogyan tudodsz 245 évig törleszteni...</p>

<p>Ha 330.000-ből levonod a 30.000-et, az még mindig sok, igaz? Én úgy vagyok vele, hogy kaján nem spórolok, minden nap van gyümölcs, zöldség, hús az asztalon, az édességről ha lehet próbálok leszokni. Így hármunkra (baba, barátnőm, én) a kaja költség 1.500 reggeli 2.500 ebéd, 2.000 vacsora, ami napi 6.000 Ft. Havonta 6.000 * 30 = 180.000 Ft. Emellé jönnek a számlák ami átlagban 30.000 (váz, gáz, áram, net, telefon, télen fűttés), 50.000 albi.</p>

<p>Családfenntartóként marad 40.000 Ft-om, és nem költöttem benzinre, ruhára, szórakozásra, egészségügyre, kondira, a kertre, kutyára, és még sorolhatnám. Az a pénz ami az anyáknak jár az első 2 évben, az elmegy az anyára és a babára.</p>

<p>Hitelt akkor is fizetned kell amikor fél évre táppénzre kerülsz és a fizetésednek csak egy részét kapod, és akkor is, ha megszűnik a munkahelyed.</p>

<p>Akkor hogyan is érdemes a hitelt visszafizetni?</p>

<h3>Diákhitel visszafizetése</h3>

<p>Mindenképpen azt javasoljuk, hogy először képezz egy kisebb vésztartalékot (amiből legrosszabb esetben is 1-2 hónapig megélsz), hogy ha beüt a krach, ne kelljen újabb hitelt felvenned.</p>

<p>Ezt követően, amennyiben több hitellel is rendelkezel, a következő megoldást érdemes alkalmazni:</p>

<ul>
    <li>Rendezd a hiteleid növekvő sorrendbe</li>
    <li>Minden hiteledre a minimum törlesztőt fizesd, kivéve a legnagyobb kamatozásút</li>
    <li>Erre a lehető legtöbbet fizesd, amit nélkülözni tudsz a mindennapjaidból</li>
    <li>Ha sikerült visszafizetned, ugorj a következő legnagyobb kamatozásúra</li>
</ul>

<p>Ezt adósság lavina módszernek hívják, <x-a href="https://banknavigator.hu/mi-a-kulonbseg-a-hogolyo-es-a-lavina-adossagkifizetesi-modszerek-kozott/" target="_blank">itt tudsz róla bővebben olvasni</x-a>.</p>

<p>Diákhitel esetén a Diákhitel 1-et és 2-őt is el kell kezdened törleszteni, ha megszűnik a hallgatói jogviszonyod. Azt tanácsoljuk, hogy amíg a Diákhitel 2 kamatmentes, arra csak a minimális összeget fizesd, míg a másikra fizess annyit, amennyit csak tudsz.</p>

<p>A hitel olyan, mint úszás közben a nyakadba akasztott vaslánc: egészen addig a víz felett tudod tartani a fejed amíg tükörsima a felszín, de ha jön a vihar és átcsapnak a fejed felett a hullámok, már azt kívánod, hogy bárcsak levetted volna azt a láncot amíg jó volt az idő.</p>

<p>Hatalmas segítség, igaz? Hát, nem. Inkább ilyen nesze neked semmi életbölcsesség. A legfontosabb mégis az, hogy kezdj minél hamarabb, és lehetőleg minél többet törleszteni, ebben pedig nagy segítségedre lehet egy ilyen pénzbeosztós költségvetés készítő, mint amilyen a beosztom.hu is.</p>

<p>Nyilván mondhatod, hogy a saját malmunkra akarjuk hajtani a vizet, de pont ezért hagyunk fél évet a használatra fizetés előtt. Meggyőződésünk, hogy ha előre beosztod a pénzed, sokkal stresszmentesebb, biztonságosabb és tervezhetőbb életed lesz. Érdemes ezt minél hamarabb elkezdeni. Akkor is, ha nem a miénket választod, hanem egy egyszerű négyzetrácsos füzetet vagy excel táblázatot.</p>
